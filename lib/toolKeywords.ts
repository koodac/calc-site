/**
 * 슬러그에 해당하는 SEO 태그 배열 반환
 */
export function getToolKeywords(slug: string): string[] {
  const prefix = slug.split("-")[0];
  const base = CATEGORY_BASE_TAGS[prefix] ?? ["계산기"];
  const specific = TOOL_KEYWORDS[slug] ?? [];
  // 중복 제거 후 반환 (specific 먼저)
  const all = [...new Set([...specific, ...base])];
  return all.slice(0, 12); // 최대 12개
}
